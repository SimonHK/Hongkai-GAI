package com.graphai.pojo;


public class Crawlercontent {

    private String id;

    private String crawlerurl;

    private String crawlercontent;

    private String crawlertime;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCrawlerurl() {
        return crawlerurl;
    }

    public void setCrawlerurl(String crawlerurl) {
        this.crawlerurl = crawlerurl;
    }

    public String getCrawlercontent() {
        return crawlercontent;
    }

    public void setCrawlercontent(String crawlercontent) {
        this.crawlercontent = crawlercontent;
    }

    public String getCrawlertime() {
        return crawlertime;
    }

    public void setCrawlertime(String crawlertime) {
        this.crawlertime = crawlertime;
    }
}

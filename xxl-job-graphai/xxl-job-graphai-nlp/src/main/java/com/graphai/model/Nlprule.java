package com.graphai.model;

public class Nlprule {

    private String id;

    private String rulename;

    private String ruleformula;

    private String ruletype;

    private String ruletime;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRulename() {
        return rulename;
    }

    public void setRulename(String rulename) {
        this.rulename = rulename;
    }

    public String getRuleformula() {
        return ruleformula;
    }

    public void setRuleformula(String ruleformula) {
        this.ruleformula = ruleformula;
    }

    public String getRuletype() {
        return ruletype;
    }

    public void setRuletype(String ruletype) {
        this.ruletype = ruletype;
    }

    public String getRuletime() {
        return ruletime;
    }

    public void setRuletime(String ruletime) {
        this.ruletime = ruletime;
    }
}

package com.graphai.model;

public class PubEnvironment {

    private String id;

    private String nlpname;

    private int nationid;

    private int ienvironid;

    private int naturlenvid;

    private int industryid;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNlpname() {
        return nlpname;
    }

    public void setNlpname(String nlpname) {
        this.nlpname = nlpname;
    }

    public int getNationid() {
        return nationid;
    }

    public void setNationid(int nationid) {
        this.nationid = nationid;
    }

    public int getIenvironid() {
        return ienvironid;
    }

    public void setIenvironid(int ienvironid) {
        this.ienvironid = ienvironid;
    }

    public int getNaturlenvid() {
        return naturlenvid;
    }

    public void setNaturlenvid(int naturlenvid) {
        this.naturlenvid = naturlenvid;
    }

    public int getIndustryid() {
        return industryid;
    }

    public void setIndustryid(int industryid) {
        this.industryid = industryid;
    }
}

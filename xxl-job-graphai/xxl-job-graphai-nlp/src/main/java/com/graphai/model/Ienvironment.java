package com.graphai.model;

public class Ienvironment {

    private String id;

    private String nlptime;

    private String nlpcontent;

    private String nlptype;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNlptime() {
        return nlptime;
    }

    public void setNlptime(String nlptime) {
        this.nlptime = nlptime;
    }

    public String getNlpcontent() {
        return nlpcontent;
    }

    public void setNlpcontent(String nlpcontent) {
        this.nlpcontent = nlpcontent;
    }

    public String getNlptype() {
        return nlptype;
    }

    public void setNlptype(String nlptype) {
        this.nlptype = nlptype;
    }
}
